    private void BsearchActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_BsearchActionPerformed
        if (!TFeng.getText().isEmpty() && !TFvnese.getText().isEmpty()) {
            Word addWord = new Word(list.size(), TFeng.getText(), TFvnese.getText());
            list.add(addWord);
            Collections.sort(list, new WordComparator());
            System.out.println("========================================");
            for (Word word : list) {
                word.hienthi();
            }
        } else {
            JOptionPane.showMessageDialog(this, "hãy nhập đủ thông tin");
        }
    }// GEN-LAST:event_BsearchActionPerformed

