    public int search() {
        int L = 0;
        int R = list.size() - 1, M;
        while (L <= R) {
            M = (L + R) / 2;
            String word = list.get(M).getEn();
            // System.out.println(TFeng.getText().trim().compareToIgnoreCase(word));
            if (TFeng.getText().trim().compareToIgnoreCase(word) == 0) {
                return M;
            } else if (TFeng.getText().trim().compareToIgnoreCase(word) > 0) {
                L = M + 1;
            } else {
                R = M - 1;
            }
        }
        return -1;

    }

