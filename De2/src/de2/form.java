    public void creatContentTable() {
        DefaultTableModel model = new DefaultTableModel();
        List<NhanVien> list = readFile();
        if (list != null) {
            for (NhanVien nhanVien : list) {
                Object[] obj = {nhanVien.getHoTen(),nhanVien.getNgaySinh()
                                ,nhanVien.getDiaChi(),nhanVien.getGioiTinh()
                                ,nhanVien.getPhongBan(),nhanVien.getHeSoLuong()
                                ,nhanVien.getLuongCoBan(),nhanVien.getThamNien(),nhanVien.luongThucLinh()};
                model = (DefaultTableModel) TableNhanVien.getModel();
                model.addRow(obj);
            }
        }else{
            JOptionPane.showMessageDialog(this, "doc file khong thanh cong hoac file trong");
        }
    }
