    public List<NhanVien> readFile(){
        FileReader fileReader = null;
        BufferedReader reader = null;
        List<NhanVien> listNhanVien = null;
        try {

            fileReader = new FileReader("/home/honahl/Documents/finalOopTrain/De2/src/de2/nhanvien.dat");
            reader = new BufferedReader(fileReader);
            listNhanVien = new ArrayList<>();

            String SNhanVien = "";
            while ((SNhanVien = reader.readLine()) != null) {
                String[] TTnhanVien = SNhanVien.split("\\$");
                NhanVien nhanVien = new NhanVien();
                nhanVien.setHoTen(TTnhanVien[0]);
                nhanVien.setGioiTinh(TTnhanVien[1]);
                nhanVien.setDiaChi(TTnhanVien[2]);
                nhanVien.setNgaySinh(TTnhanVien[3]);
                nhanVien.setPhongBan(TTnhanVien[4]);
                nhanVien.setThamNien(Integer.parseInt(TTnhanVien[5]));
                nhanVien.setHeSoLuong(Integer.parseInt(TTnhanVien[6]));
                nhanVien.setLuongCoBan(Integer.parseInt(TTnhanVien[7]));
                listNhanVien.add(nhanVien);
                System.out.println("doc file thanh cong");
            }
            System.out.println("doc file thanh cong");
            fileReader.close();
            reader.close();
        } catch (Exception e) {
            System.out.println("doc file khong thanh cong");
            e.printStackTrace();
        }
        return listNhanVien;
    }
    
